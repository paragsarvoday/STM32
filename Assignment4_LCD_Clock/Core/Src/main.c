//PIN15 for set button (cycle between setting cursor location to seconds, minutes, hrs
//						and alarm ON or OFF)
//PIN10 for increment button

void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
 /* Prevent unused argument(s) compilation warning */
 UNUSED(GPIO_Pin);
 /* NOTE: This function Should not be modified, when the callback is needed,
          the HAL_GPIO_EXTI_Callback could be implemented in the user file
  */
 current_time = HAL_GetTick();

 if(alarm_detected > 0){
	 if(((GPIO_Pin == GPIO_PIN_13) |  (GPIO_Pin == GPIO_PIN_15) | (GPIO_Pin == GPIO_PIN_10)) && (current_time - previous_time > debounce_time) )
	 alarm_detected = 0;
	 previous_time = current_time;
 }

 if(GPIO_Pin == GPIO_PIN_13 && current_time - previous_time > debounce_time){

	 mode = (mode + 1)%3;
	 cursor_loc = 0;
	 previous_time = current_time;
 }

 if(GPIO_Pin == GPIO_PIN_15 && mode!=1 && current_time - previous_time > debounce_time){
	 if(mode == 0){
		 cursor_loc = (cursor_loc+1)%3;
	 }
	 if(mode == 2){
		 cursor_loc = (cursor_loc+1)%4;
	 }
	 previous_time = current_time;
 }

 if(GPIO_Pin == GPIO_PIN_10 && mode!=1 && current_time - previous_time > debounce_time){
	 if(mode == 0){
		 if(cursor_loc == 0){
			 tim_sec = 0;
		 }
		 if(cursor_loc == 1){
			 tim_min = (tim_min+1)%60;
		 }
		 if(cursor_loc == 2){
			 tim_hrs = (tim_hrs+1)%24;
		 }
	 }
	 if(mode == 2){
		 if(cursor_loc == 0){
					 alarm_sec = (alarm_sec+1)%60;
				 }
		 if(cursor_loc == 1){
			 alarm_min = (alarm_min+1)%60;
		 }
		 if(cursor_loc == 2){
			 alarm_hrs = (alarm_hrs+1)%24;
		 }
		 if(cursor_loc == 3){
			 alarm_on_off = !alarm_on_off;
		 }
	 }
	 previous_time = current_time;
 }


}
