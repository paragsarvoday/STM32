void display_clock(void){

	//a function to indicate that time==alarm time when alarm_on_off == 1

	if (mode!=2){
	lcd_put_cur(0, 0);
	sprintf(time_array, "%02d:%02d:%02d" , tim_hrs , tim_min, tim_sec);
	lcd_send_string(time_array);
	}
	if (mode==0){
		lcd_put_cur(1, 0);
		lcd_send_string("SET TIME  ");
	}
	if (mode==1 && alarm_detected==0){
		lcd_put_cur(1, 0);
		lcd_send_string("CLOCK    ");
		HAL_GPIO_WritePin(GPIOB, LD1_Pin, 0);
		HAL_GPIO_WritePin(GPIOB, LD2_Pin, 0);
		HAL_GPIO_WritePin(GPIOB, LD3_Pin, 0);
	}
	if (mode==2){
		lcd_put_cur(0, 0);
		sprintf(time_array, "%02d:%02d:%02d" , alarm_hrs , alarm_min, alarm_sec);
		lcd_send_string(time_array);
		lcd_put_cur(1, 0);
		lcd_send_string("SET ALARM");

	}
	if (alarm_on_off){
		lcd_put_cur(1, 13);
		lcd_send_string("ON ");
	}
	if (!alarm_on_off){
		lcd_put_cur(1, 13);
		lcd_send_string("OFF");
	}
	if (alarm_detected>0){
		lcd_put_cur(1, 0);
		lcd_send_string("ALARM!     ");
	}

}


